          <h2>Te ayudamos a ser la gran mujer que llevas dentro. Concurso #MeSientoPoderosa</h2>  
          
         

<p>Celebremos el día internacional de la mujer escuchando las razones para sentirnos poderosas. ¡Cuéntanos y cuéntale al mundo qué te empodera, y participa por un Día Personaling (Personaling Day) en el que nuestros Personal Shoppers te transformarán de pies a cabeza y te ayudarán a definir tu estilo personal!</p>

<p>Dinos en un tuit qué te hace sentir poderosa, o cuándo te sientes poderosa, completando la frase #MeSientoPoderosa…</p>

<p>Por ejemplo: <i> #MeSientoPoderosa cuando hago escuchar mi voz y digo mis opiniones @PersonalingVzla; #MeSientoPoderosa porque todos los días escojo mis batallas y los looks para darlas @PersonalingVzla; #MeSientoPoderosa al pintar mis labios cada mañana @PersonalingVzla</i></p>

<p>La frase más original será la ganadora, ¡así que usa toda tu creatividad! </p>

<p>Chévere, ¿verdad?</p>

 <p class="text_align_center margin_top margin_bottom">
