        </p>
 
<p>Éstas son las bases del concurso:</p>
 <div>
     <ol>
         <li>
             El concurso tendrá lugar en twitter. <b>Se abrirá el domingo 8 de marzo (Día Internacional de la Mujer) a las 8am, y cerrará el viernes 13 de marzo a las 8pm hora de Caracas, Venezuela. Sólo se tomarán en cuenta los tuits recibidos en ese rango de tiempo.</b>
        </li>
         
          <li>
             Sólo podrán participar mujeres que residan en el territorio de la República Bolivariana de Venezuela.
         </li>
         
         <li>
              Para que tu participación sea tomada en consideración <b>debes incluir en tu tuit tanto el hashtag #MeSientoPoderosa, como una mención a @PersonalingVzla</b>
          </li>
         
          <li>
             Puedes participar enviando la cantidad de tuits que desees, pero en la selección de los tuits finalistas tomaremos en cuenta uno (1) solo por participante. 
          </li>
         
          <li>
              No calificarán a los efectos de este concurso tuits con contenido ofensivo, obsceno, ligados a la política, discriminatorios por cualquier motivo, con mensajes de odio o que inciten a la violencia.
         </li>
          
          <li>
              Una vez cerrado el concurso el viernes 13 de marzo a las 8pm, nuestro panel de expertos revisará todos los tuits recibidos y seleccionará los diez (10) tuits más creativos sobre las razones que nos hacen sentir poderosas, y con mayor actividad en Twitter en el lapso de duración del concurso.

          </li>
          
          <li>
              El martes 17 de marzo los diez (10) tuits más creativos sobre las razones que nos hacen sentir poderosas (#TOP10), serán publicados en nuestra página Web <a href="www.personaling.com.ve">www.personaling.com.ve</a> y allí serán sometidos a votación libre por el público.

          </li>
          <li>
              El tuit que reciba mayor número de votos será el ganador, y su autora ganará un Día Personaling (Personaling Day) para una transformación de pies a cabeza.

          </li>
          <li>
              Las autoras de los nueve tuits finalistas recibirán un bono de Bs.200 en su cuenta Personaling para consumir en compras de artículos o looks de nuestra tienda.

          </li>
          <li>
              La votación por los tuits ganadores se llevará a cabo en nuestra página Web www.personaling.com.ve entre los días 17 de marzo de 2015 y 20 de marzo de 2015.

          </li>
          <li>
              La ganadora del premio principal, autora del tuit con mayor cantidad de votos, será anunciada el lunes 23 de marzo de 2015 vía Twitter y llamada telefónica a la ganadora. 

          </li>
          <li>
              El premio principal, un Día Personaling (Personaling Day), consistirá en lo siguiente:
              <ol type="a">
                  <li>
                     Un look Personaling (outfit completo) de 4 piezas equivalente por valor de Bs10.000,00, seleccionado por nuestros Personal Shoppers de acuerdo al tipo de cuerpo y estilo de la ganadora.
                   </li>
                   <li>
                     Sesión de peluquería: manicure, pedicure, maquillaje, corte de pelo, y peinado con las orientaciones de nuestro estilista (Personal Shopper).
  
                   </li>
                   <li>
                      Una entrevista en video a la ganadora del Concurso #MeSientoPoderosa, y reseña en nuestra Magazine Personaling de la experiencia del <b>Día Personaling</b> (Personaling Day).
 
                   </li>
                   
                  
              </ol>

          </li>
          <li>
             El premio principal y los premios a las finalistas son intransferibles. 
          </li>
          <li>
             En caso que la ganadora desista de reclamar el premio, éste se le otorgará a la 2da frase más votada y así sucesivamente hasta otorgar el premio definitivamente. 
          </li>
      </ol>
      
